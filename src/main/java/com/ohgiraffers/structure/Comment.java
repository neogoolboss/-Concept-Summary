package com.ohgiraffers.structure;

public class Comment {

    public static void main(String[] args) {

        /* 자바 코드에 프로그래머의 의견이나 코드의 설명을 적을 수 있는데 이것을 주석(Comment)이라고 한다.
        * 컴파일 시에 주석은 자동으로 바이너리 코드에서 제거되기 때문에 프로그램을 수행하는 데 전혀 지장이 없다.
        * */

        /* 1. 블록 주석
        *   '/*' 가 블록 주석의 시작이고 '*\/' 가 블록 주석으 ㅣ끝이다.
        *   블록 주석은 코드에서 블록 단위(클래스, 메소드 등)로 설명할 때 주로 사용한다.
        * */

        /* 2. 라인주석
        *   라인주석은 // 기호를 사용하는데, //가 시작된 곳부터 그 라인의 끝까지 주석문이 된다.
        *   보통 특정코드 한 줄을 설명할 때 라인 주석을 사용한다.
        * */

    }

}
