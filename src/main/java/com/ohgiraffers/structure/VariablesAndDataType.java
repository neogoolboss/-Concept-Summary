package com.ohgiraffers.structure;

public class VariablesAndDataType {

    public static void main(String[] args) {

        /* 변수는 흔히 ㄱ밧을 넣을 수 있는 빈 상자와 같다고 표현한다.
        *  즉, 변수는 값을 대입하여 사용할 수 있다.
        *  값에는 어떤 형태가 정해져야 하는데, 그 형태가 바로 자료형이다.
        *
        *     int      a;
        *   [자료형]  [변수]
        *
        * */

        /* 변수명을 지을 때는 3가지 규칙을 따라야 한다.
        *   1. 변수명은 숫자로 시작할 수 없다.  ex) int 1st;
        *   2. '_'와 '$' 이외의 특수문자는 사용할 수 없다.  ex) int a#;
        *   3. int, class, return 등 자바의 키워드는 변수명으로 사용할 수 없다.  ex) int class;;
        * */

        /* 자바의 키워드 */
        /*      abstract           continue           for           new           switch
        *       assert             default            goto          package       synchronized
        *       boolean            do                 if            private       this
        *       break              double             implements    protected     throw
        *       byte               else               import        public        throws
        *       case               enum               instanceof    return        transient
        *       catch              extends            int           short         try
        *       char               final              interface     static        void
        *       class              finally            long          strictfp      volatile
        *       const              float              native        super         while
        * */

        /* 자주 쓰이는 자로형
        *  - int        - String
        *  - long       - StringBuffer
        *  - double     - List
        *  - boolean    - Map
        *  - char       - Set
        *  - float      - short
        * */
    }

}
